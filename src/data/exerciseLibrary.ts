// Helper functions
export function getExerciseById(id: string): ExerciseDefinition | undefined {
  return EXERCISE_LIBRARY.find(ex => ex.id === id);
}

export function getExercisesByCategory(category: string): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter(ex => ex.category === category);
}

export function getExercisesByMuscleGroup(muscleGroup: string): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter(ex => ex.muscleGroups.includes(muscleGroup));
}

export function getExercisesByDifficulty(difficulty: string): ExerciseDefinition[] {
  return EXERCISE_LIBRARY.filter(ex => ex.difficulty === difficulty);
}

export function searchExercises(query: string): ExerciseDefinition[] {
  const lowerQuery = query.toLowerCase();
  return EXERCISE_LIBRARY.filter(ex =>
    ex.name.toLowerCase().includes(lowerQuery) ||
    ex.description.toLowerCase().includes(lowerQuery) ||
    ex.muscleGroups.some(mg => mg.toLowerCase().includes(lowerQuery)) ||
    ex.category.toLowerCase().includes(lowerQuery)
  );
}
